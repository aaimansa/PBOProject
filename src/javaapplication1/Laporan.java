    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        if(sehat.isSelected())  {
            KondisiFisik1.append("Sehat");
        }
        if(demam.isSelected())  {
            KondisiFisik1.append("Demam\n");
        }
        if(batuk.isSelected())  {
            KondisiFisik1.append("Batuk\n");
        }
        if(pilek.isSelected())  {
            KondisiFisik1.append("Pilek\n");
        }
        if(sesak.isSelected())  {
            KondisiFisik1.append("Sesak nafas\n");
        }
        if(hidung.isSelected()) {
            KondisiFisik1.append("Hidung tidak dapat membau\n");
        }
        if(sakit.isSelected())  {
            KondisiFisik1.append("Sakit tenggorokan\n");
        }
        if(lainlain1.isSelected())  {
            kondisilain = lainlain2.getText();
            KondisiFisik1.append(kondisilain);
        }
        
        
        if (membatasi.isSelected()) {
            Aktivitas1.append("Membatasi keluar rumah\n");
        }
        if(tidak.isSelected())  {
            Aktivitas1.append("Tidak mengunjungi kerabat yang sakit\n");
        }
        if(menjaga.isSelected())    {
            Aktivitas1.append("Menjaga jarak yang aman saat berinteraksi\n");
        }
        
        Integer push = (Integer) pushup.getValue();
        String p = push.toString();
        pushup1.setText(p);
        
        Integer sit = (Integer) situp.getValue();
        String s = sit.toString();
        situp1.setText(s);
        
        Integer jog = (Integer) jogging.getValue();
        String j = jog.toString();
        jogging1.setText(j);

        lainlainolahraga = lainlainol1.getText();
        lainlainol2.setText(lainlainolahraga);
        
        matkul11.setText(kuliah1);
        
        if(kuliah1=="Tidak Ada")
        {
            kuliah2="";
            me1="";
            me2="";
            rangkum1="";
            rangkum2="";
            dead1="";
            dead2="";
        }
        else
        {
        matkul21.setText(kuliah2);
        me1 = media1.getText();
        media11.setText(me1);
        me2 = media2.getText();
        media21.setText(me2);
        rangkum1 = rangkuman1.getText();
        rangkuman11.setText(rangkum1);
        rangkum2 = rangkuman2.getText();
        rangkuman21.setText(rangkum2);
        dead1 = deadline1.getText();
        deadline11.setText(dead1);
        dead2 = deadline2.getText();
        deadline21.setText(dead2);
        }
    }//GEN-LAST:event_simpanActionPerformed

